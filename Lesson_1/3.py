# 3.	По введенным пользователем координатам двух точек вывести
# уравнение прямой вида y = kx + b, проходящей через эти точки.

x1 = float(input(f'''Введите координаты первой точки
x1:'''))
y1 = float(input('y1:'))
x2 = float(input(f'''Введите координаты второй точки
x2:'''))
y2 = float(input('y2:'))
digit = '+' if (x1*y2-x2*y1)/(x2-x1)>=0 else '-'
print(f'y = {(-y1+y2)/(x2-x1)}x {digit} {abs((x1*y2-x2*y1)/(x2-x1))}')
